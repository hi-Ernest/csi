package edu.team.entity;

/**
 * 初始项目实体类示例
 * @author dailiwen
 * @date 2019/06/21
 */
public class User {
    private String id;
    private String name;
    private String remake;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRemake() {
        return remake;
    }

    public void setRemake(String remake) {
        this.remake = remake;
    }
}
